from langchain_openai import ChatOpenAI
from langchain_core.prompts import PromptTemplate
from langchain_core.output_parsers import StrOutputParser
from langchain_core.runnables import RunnablePassthrough
from sentence_transformers import CrossEncoder

# cross-encoder reranker model
reranker = CrossEncoder("cross-encoder/ms-marco-MiniLM-L-6-v2")

# OpenAI model
llm = ChatOpenAI(
    model="gpt-4o-mini",
    temperature=0.2,
    api_key=os.environ.get("OPENAI_API_KEY")
)

prompt_template = PromptTemplate.from_template("""
You are a test maker chatbot for a university course.

Use ONLY the retrieved course materials below to answer the request.
Do NOT use external knowledge.
Be clear and concise.
If the answer is not in the context, say: "I couldn't find that in the course materials."

When generating answers or questions:
Base them strictly on the context.

If the user asks for practice questions, a quiz, or a mini test:
Generate the requested number of questions.
Include both multiple choice and open-ended questions when possible.
After all questions, provide an Answer Key section.
For multiple choice questions, provide the correct letter and the text.
For open-ended questions, provide a short model answer.

At the end of every response, include a section exactly called:
Sources:
and list the source files you used.

Context:
--------
{context}
--------

Question: {question}

Answer:
""")

retriever = vectorstore.as_retriever(search_kwargs={"k": 10})

def rerank_documents(query, docs, top_k=4):
    pairs = [[query, doc.page_content] for doc in docs]
    scores = reranker.predict(pairs)

    ranked = sorted(
        zip(scores, docs),
        key=lambda x: x[0],
        reverse=True
    )

    top_docs = [doc for _, doc in ranked[:top_k]]
    return top_docs

def format_docs(query):
    docs = retriever.invoke(query)
    docs = rerank_documents(query, docs, top_k=4)

    formatted = []
    for doc in docs:
        source = doc.metadata.get("source", "unknown")
        doc_type = doc.metadata.get("document_type", "unknown")
        sheet_name = doc.metadata.get("sheet_name", None)

        header = f"[Source: {source} | Type: {doc_type}"
        if sheet_name not in [None, "None", "unknown"]:
            header += f" | Sheet: {sheet_name}"
        header += "]"

        formatted.append(f"{header}\n{doc.page_content}")

    return "\n\n".join(formatted)

qa_chain = (
    {"context": format_docs, "question": RunnablePassthrough()}
    | prompt_template
    | llm
    | StrOutputParser()
)

print("✅ Chatbot ready with OpenAI + reranker!")

llm.invoke("Explain PCA in one sentence")

answer = qa_chain.invoke("What is PCA?")
print(answer)

