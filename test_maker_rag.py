def ask_bot():
    print("\n🎓 Test Maker Chatbot active!")
    print('Ask for a concept explanation, a practice question, or a mini test.')
    print('Type "exit" to stop.\n')

    while True:
        query = input("Your request: ").strip()

        if not query:
            continue

        if query.lower() in ("exit", "quit"):
            print("👋 Goodbye!")
            break

        try:
            answer = qa_chain.invoke(query)
            print(f"\n🤖 Response:\n{answer}")
            print("\n" + "-" * 60)

        except Exception as e:
            print(f"\n❌ Error: {e}\n")

ask_bot()

