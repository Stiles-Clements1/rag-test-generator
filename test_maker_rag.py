import os
import pandas as pd
from langchain_text_splitters import RecursiveCharacterTextSplitter
from langchain_core.documents import Document


def infer_document_type(file_name):
    file_lower = file_name.lower()

    if file_lower.endswith(".r"):
        return "code"
    elif file_lower.endswith(".csv") or file_lower.endswith(".xlsx") or file_lower.endswith(".xls"):
        return "tabular_data"
    elif "slides" in file_lower:
        return "slides"
    else:
        return "notes"


def process_course_materials(directory_path):
    # Try Docling first (best for PDFs with images/tables)
    try:
        from docling.document_converter import DocumentConverter
        converter = DocumentConverter()
        use_docling = True
        print("📄 Using Docling for PDF extraction (best quality)")
    except Exception:
        use_docling = False
        print("📄 Using pypdf for PDF extraction (fallback)")

    text_splitter = RecursiveCharacterTextSplitter(
        chunk_size=1000,
        chunk_overlap=150,
        separators=["\n\n", "\n", " ", ""]
    )

    supported = (".pdf", ".r", ".xlsx", ".xls", ".csv")
    all_files = [f for f in os.listdir(directory_path) if f.lower().endswith(supported)]

    if not all_files:
        print(f"⚠️ No supported files found in: {directory_path}")
        print(f"Files present: {os.listdir(directory_path)}")
        return []

    print(f"\n📂 Found {len(all_files)} file(s): {all_files}\n")
    all_docs = []

    for file in all_files:
        file_path = os.path.join(directory_path, file)
        text = ""

        try:
            ext = file.lower().rsplit(".", 1)[-1]
            document_type = infer_document_type(file)

            if ext == "pdf":
                if use_docling:
                    result = converter.convert(file_path)
                    text = result.document.export_to_markdown()
                else:
                    from pypdf import PdfReader
                    reader = PdfReader(file_path)
                    text = "\n\n".join(p.extract_text() or "" for p in reader.pages)

            elif ext == "r":
                with open(file_path, "r", encoding="utf-8", errors="replace") as f:
                    raw_code = f.read()
                    text = (
                        f"Document type: R code\n"
                        f"Source file: {file}\n"
                        f"This file contains R code, functions, scripts, or implementation examples.\n\n"
                        f"{raw_code}"
                    )

            elif ext in ("xlsx", "xls"):
                df_dict = pd.read_excel(file_path, sheet_name=None)
                excel_chunks = []

                for sheet_name, df in df_dict.items():
                    sheet_text = (
                        f"Document type: spreadsheet\n"
                        f"Source file: {file}\n"
                        f"Sheet name: {sheet_name}\n"
                        f"This sheet contains tabular data, numerical values, or examples.\n\n"
                        f"{df.to_string(index=False)}"
                    )
                    excel_chunks.append((sheet_name, sheet_text))

            elif ext == "csv":
                df = pd.read_csv(file_path)
                text = (
                    f"Document type: csv table\n"
                    f"Source file: {file}\n"
                    f"This file contains tabular data, numerical values, or dataset examples.\n\n"
                    f"{df.to_string(index=False)}"
                )

            # Handle Excel separately so each sheet becomes its own source block
            if ext in ("xlsx", "xls"):
                for sheet_name, sheet_text in excel_chunks:
                    if not sheet_text.strip():
                        continue

                    sheet_text = sheet_text.replace("\n\n\n", "\n\n").replace("  ", " ")
                    chunks = text_splitter.split_text(sheet_text)

                    for i, chunk in enumerate(chunks):
                        all_docs.append(
                            Document(
                                page_content=chunk,
                                metadata={
                                    "source": file,
                                    "document_type": document_type,
                                    "file_extension": ext,
                                    "sheet_name": sheet_name,
                                    "chunk_id": i,
                                    "total_chunks": len(chunks)
                                }
                            )
                        )

                    print(f"  ✅ {file} | sheet={sheet_name}: {len(chunks)} chunk(s)")

                continue

            if not text.strip():
                print(f"  ⚠️ {file}: empty after extraction, skipping")
                continue

            text = text.replace("\n\n\n", "\n\n").replace("  ", " ")
            chunks = text_splitter.split_text(text)

            for i, chunk in enumerate(chunks):
                all_docs.append(
                    Document(
                        page_content=chunk,
                        metadata={
                            "source": file,
                            "document_type": document_type,
                            "file_extension": ext,
                            "sheet_name": None,
                            "chunk_id": i,
                            "total_chunks": len(chunks)
                        }
                    )
                )

            print(f"  ✅ {file}: {len(chunks)} chunk(s)")

        except Exception as e:
            print(f"  ❌ {file}: Error — {e}")

    print(f"\n📊 Total chunks ready for indexing: {len(all_docs)}")
    return all_docs


docs = process_course_materials(COURSE_FOLDER)

